document.querySelector('.burger').onclick = function() {
  document.querySelector('.burger').style.display = `none`
  document.querySelector('.cross').style.display = `block`
  document.querySelector('.fullmenu').style.display = `flex`
}

document.querySelector('.cross').onclick = function() {
  document.querySelector('.burger').style.display = `inherit`
  document.querySelector('.cross').style.display = `none`
  document.querySelector('.fullmenu').style.display = `none`
}

document.querySelector(".hide").onclick = function() {
  document.querySelector('.fullmenu').style.display = `none`
  document.querySelector('.burger').style.display = `inherit`
  document.querySelector('.cross').style.display = `none`
}
